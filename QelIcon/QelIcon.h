    QelIcon(Icon icon, int size = 16, const QColor &color = Qt::black)
    {
        // 加载 FontAwesome 字体
        static QString fontFamily = loadFontAwesome();

        if (!fontFamily.isEmpty()) {
            QFont font(fontFamily);
            font.setPointSize(size);

                    // 创建一个 QPixmap，用于绘制图标
            QPixmap pixmap(size, size);
            pixmap.fill(Qt::transparent);

                    // 使用 QPainter 绘制图标
            QPainter painter(&pixmap);
            painter.setFont(font);
            painter.setPen(color);
            painter.drawText(pixmap.rect(), Qt::AlignCenter, QChar(icon));

                    // 将绘制的图标作为 QIcon 的实例
            this->addPixmap(pixmap);
        }
    }

private:
    static QString loadFontAwesome()
    {
        static bool loaded = false;
        static QString family;

        if (!loaded) {
            int id = QFontDatabase::addApplicationFont(":/fonts/fonts/fontawesome-4.7.0.ttf");
            if (id != -1) {
                family = QFontDatabase::applicationFontFamilies(id).at(0);
            } else {
                qWarning() << "Failed to load FontAwesome font!";
            }
            loaded = true;
        }

        return family;
    }
